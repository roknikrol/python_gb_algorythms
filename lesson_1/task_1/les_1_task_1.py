#	1. Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6.
#	Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.

print('Побитовые операции на числами 5 и 6.')
a = 5
b = 6

# операция И
print('Операция И:', a & b)
# операция ИЛИ
print('Операция ИЛИ:', a | b)
# сдвиг 5 влево на 2 знака
print('Сдвиг 5 влево на 2 знака:', a << 2)
# сдвиг 5 вправо на 2 знака
print('Сдвиг 5 вправо на 2 знака:', a >> 2)



